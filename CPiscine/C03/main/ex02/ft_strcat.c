char	*ft_strcat(char *dest, char *src)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = dest;
	while (*dest != '\0')
		dest++;
	while (*(src + i) != '\0')
	{
		*(dest + i) = *(src + i);
		i++;
	}
	*(dest + i) = '\0';
	return (ptr);
}
