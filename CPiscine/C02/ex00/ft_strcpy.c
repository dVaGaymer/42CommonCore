#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int index;

	index = 0;
	while (*(src + index) != '\0')
	{
		*(dest + index) = *(src + index);
		index++;
	}
	*(dest + index) = '\0';
	return (dest);
}

int main()
{
	char s[] = "hola";
	char d[5];
	ft_strcpy(d, s);
	
		printf(" %s ", d);
}
