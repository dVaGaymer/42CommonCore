int	ft_is_prime(int nb)
{
	int index;

	index = 3;
	if (nb % 2 == 0)
		return (0);
	while (index < nb / 2)
	{
		if (nb % index == 0)
			return (0);
		index += 2;
	}
	return (1);
}
