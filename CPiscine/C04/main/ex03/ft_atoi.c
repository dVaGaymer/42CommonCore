int	ft_atoi(char *str)
{
	int sign;
	int num;

	num = 0;
	sign = 0;
	while (*str == 32 || *str == '\t' || *str == '\n'
			|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	while (*str == 43 || *str == 45)
		if (*str++ == 45)
			sign++;
	sign = (sign % 2 != 0) ? -1 : 1;
	while (*str >= 48 && *str <= 57)
		num = (num * 10) + (*(str++) - 48);
	return (num * sign);
}
