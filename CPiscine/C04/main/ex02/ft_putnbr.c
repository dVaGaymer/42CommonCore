#include <unistd.h>

void	ft_putnbr(int nb)
{
	long num;

	num = (long)nb;
	if (num < 0)
	{
		write(1, "-", 1);
		num = num * -1;
	}
	if (num > 9)
		ft_putnbr(num / 10);
	num = num % 10 + 48;
	write(1, &num, 1);
}
